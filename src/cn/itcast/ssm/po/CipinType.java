package cn.itcast.ssm.po;

public class CipinType {
	private int type_id;
	private String shop_name;
	private String cipin_type;
	private String cipin_detail;
	public int getType_id() {
		return type_id;
	}
	public void setType_id(int type_id) {
		this.type_id = type_id;
	}
	public String getShop_name() {
		return shop_name;
	}
	public void setShop_name(String shop_name) {
		this.shop_name = shop_name;
	}
	public String getCipin_type() {
		return cipin_type;
	}
	public void setCipin_type(String cipin_type) {
		this.cipin_type = cipin_type;
	}
	public String getCipin_detail() {
		return cipin_detail;
	}
	public void setCipin_detail(String cipin_detail) {
		this.cipin_detail = cipin_detail;
	}
	
}
