package cn.itcast.ssm.po;

public class CipinNew {
	private int cipin_id;
	private int track_id;
	private String cipin_type;
	private String cipin_species;
	private String cipin_num;
	public int getCipin_id() {
		return cipin_id;
	}
	public void setCipin_id(int cipin_id) {
		this.cipin_id = cipin_id;
	}
	public int getTrack_id() {
		return track_id;
	}
	public void setTrack_id(int track_id) {
		this.track_id = track_id;
	}
	public String getCipin_type() {
		return cipin_type;
	}
	public void setCipin_type(String cipin_type) {
		this.cipin_type = cipin_type;
	}
	public String getCipin_species() {
		return cipin_species;
	}
	public void setCipin_species(String cipin_species) {
		this.cipin_species = cipin_species;
	}
	public String getCipin_num() {
		return cipin_num;
	}
	public void setCipin_num(String cipin_num) {
		this.cipin_num = cipin_num;
	}
	
}
